package br.ind.lhf.introducao;

public class Operadores {

    public static void main(String[] args){

        int n1 = 20;
        int n2 = 10;
        int soma = n1 + n2;
        int sub = n1 - n2;
        float div = n1 / n2;
        int mult = n1*n2;

        System.out.println("O numero 1 é " + n1);
        System.out.println("O numero 2 é " + n2);
        System.out.println("A soma é " + soma);
        System.out.println("A subtracao é " + sub);
        System.out.println("A divisao é " + div);
        System.out.println("A multiplicacao é " + mult);

    }
}
